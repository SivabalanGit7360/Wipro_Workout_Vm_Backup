var1 = 150
print(var1)
print(type(var1)) # Integer type data

var2 = 12.24
print(var2)
print(type(var2)) # float type data

var3 = "Hello World"
print(var3)
print(type(var3)) # String type data

var4 = ("Hello","World",'a',25,50)
print(var4)
print(type(var4)) # tuple type data

var5 = ["Hello","World",'a',25,50]
print(var5)
print(type(var5)) # list type data

var6 = {'Os':'Linux','Os':'Windows','Os':'Mac'}
print(var6)
print(type(var6)) # Dictionary type data

var7 = {1,2,3,4,5}
print(var7)
print(type(var7)) # Set type data